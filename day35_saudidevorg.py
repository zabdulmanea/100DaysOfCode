# Python Lambda
# It is a small anonymous function
# Syntax: "lambda arguments: expression"

# one argument
x = lambda a : a + 10
print(x(5))

# def x(a):
#     return a + 10
# print(x(5))

# two arguments
x = lambda a, b : a * b
print(x(5, 2))

# def x(a, b):
#     return a * b
# print(x(5, 2))

# three arguments
x = lambda a, b, c : a + b + c
print(x(5,3,7))

# def x(a, b, c):
#     return a + b + c
# print(x(5,3,7))
