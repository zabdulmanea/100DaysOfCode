
def calculate(a, b, operand):
    if operand == "+":
        return a + b
    elif operand == "-":
        return a - b
    elif operand == "*":
        return a * b
    elif operand == "/":
        return a / b
    else:
        return "Error: Wrong values/operand!!"


def add(a, b):
    return a + b


def sub(a, b):
    return a - b


def multiply(a, b):
    return a * b


def divide(a, b):
    return a / b
