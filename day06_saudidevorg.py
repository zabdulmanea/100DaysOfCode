# Python Casting

# How to use int()
x = int(1)
y = int(2.976)
z = int("5")

print("Casting to integer numbers:")
print(x, y, z)

# How to use float()
x = float(2)
y = float(5.986)
z = float("9")
w = float("6.73")

print("Casting to float numbers:")
print(x, y, z, w)

# How to use str()
x = str("Zinab2019")
y = str(50)
z = str("5.97")

print("Casting to strings:")
print(x, y, z)
print(type(x), type(y), type(z))
